package day2.homeassignment.controlstatement;

public class IsPrime {

	public static void main(String[] args) {
		
		int n=13; // define value
		
		for (int i = 1; i <= n; i++) {
			
			if (n%i == 0) {
				System.out.println(i+" This is not a prime number");
			}
			else {
				System.out.println(i+" This number is a Prime number");
			}
			
		
		

	}
	}
}	


 